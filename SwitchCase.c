#include<stdio.h>
int main()
{
    int ch;
    switch(ch)
    {
        case 1:
        printf("first");
        break;

        case 2:
        printf("two");
        break;

        case 3:
        printf("three");
        break;

        case 4:
        printf("four");
        break;

        default:
        printf("hello");
    }
    return 0;
}